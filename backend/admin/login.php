                        <h3 class="text-center mb-4">Admin Login</h3>
                        <?php if (isset($error)): ?>
                            <div class="alert alert-danger"><?php echo $error; ?></div>
                        <?php endif; ?>
                        <form method="post">
                            <div class="mb-3">
                                <input type="email" name="email" class="form-control" placeholder="Email" required>
